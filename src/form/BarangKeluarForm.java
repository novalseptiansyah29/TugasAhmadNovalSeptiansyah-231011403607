private void loadBarang(){

    try{

        Connection conn =
        koneksi.getConnection();

        String sql =
        "SELECT kode_barang FROM barang";

        PreparedStatement pst =
        conn.prepareStatement(sql);

        ResultSet rs =
        pst.executeQuery();

        jComboBox1.removeAllItems();

        while(rs.next()){

            jComboBox1.addItem(
            rs.getString("kode_barang"));

        }

    }catch(Exception e){

        System.out.println(e);

    }

}

private void tampilKeluar(){

    DefaultTableModel model =
    new DefaultTableModel();

    model.addColumn("ID");
    model.addColumn("Kode Barang");
    model.addColumn("Tanggal");
    model.addColumn("Jumlah");

    try{

        Connection conn =
        koneksi.getConnection();

        String sql =
        "SELECT bk.id_keluar, " +
        "b.kode_barang, " +
        "bk.tanggal, " +
        "bk.jumlah " +
        "FROM barang_keluar bk " +
        "JOIN barang b " +
        "ON bk.id_barang=b.id_barang";

        PreparedStatement pst =
        conn.prepareStatement(sql);

        ResultSet rs =
        pst.executeQuery();

        while(rs.next()){

            model.addRow(new Object[]{

                rs.getString("id_keluar"),
                rs.getString("kode_barang"),
                rs.getString("tanggal"),
                rs.getString("jumlah")

            });

        }

        tblKeluar.setModel(model);

    }catch(Exception e){

        System.out.println(e);

    }

}
