    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        
         try{

        Connection conn =
        koneksi.getConnection();

        String kodeBarang =
        jComboBox1.getSelectedItem().toString();

        int jumlahKeluar =
        Integer.parseInt(
        txtJumlah.getText());

        // cek stok

        String sqlCek =
        "SELECT stok FROM barang " +
        "WHERE kode_barang=?";

        PreparedStatement pstCek =
        conn.prepareStatement(sqlCek);

        pstCek.setString(1,kodeBarang);

        ResultSet rs =
        pstCek.executeQuery();

        if(rs.next()){

            int stok =
            rs.getInt("stok");

            if(jumlahKeluar > stok){

                JOptionPane.showMessageDialog(
                this,
                "Stok tidak mencukupi!");

                return;
            }

        }

        String tanggal =
        new SimpleDateFormat(
        "yyyy-MM-dd")
        .format(
        cmbBarang.getDate());

        // simpan transaksi keluar

        String sqlKeluar =
        "INSERT INTO barang_keluar(id_barang,tanggal,jumlah) " +
        "SELECT id_barang, ?, ? " +
        "FROM barang WHERE kode_barang=?";

        PreparedStatement pstKeluar =
        conn.prepareStatement(sqlKeluar);

        pstKeluar.setString(1,tanggal);
        pstKeluar.setInt(2,jumlahKeluar);
        pstKeluar.setString(3,kodeBarang);

        pstKeluar.executeUpdate();

        // kurangi stok

        String sqlUpdate =
        "UPDATE barang " +
        "SET stok = stok - ? " +
        "WHERE kode_barang=?";

        PreparedStatement pstUpdate =
        conn.prepareStatement(sqlUpdate);

        pstUpdate.setInt(
        1,
        jumlahKeluar);

        pstUpdate.setString(
        2,
        kodeBarang);

        pstUpdate.executeUpdate();
        pstUpdate.executeUpdate();

        JOptionPane.showMessageDialog(
        this,
        "Barang keluar berhasil disimpan");

        txtJumlah.setText("");

    }catch(Exception e){

        JOptionPane.showMessageDialog(
        this,
        e.getMessage());

    }

    }//GEN-LAST:event_btnSimpanActionPerformed

