private void loadBarang(){

    try{

        Connection conn =
        koneksi.getConnection();

        String sql =
        "SELECT kode_barang FROM barang";

        PreparedStatement pst =
        conn.prepareStatement(sql);

        ResultSet rs =
        pst.executeQuery();

        cmbBarang.removeAllItems();

        while(rs.next()){

            cmbBarang.addItem(
            rs.getString("kode_barang"));

        }

    }catch(Exception e){

        System.out.println(e);

    }

}
private void tampilOpname(){

    DefaultTableModel model =
    new DefaultTableModel();

    model.addColumn("ID");
    model.addColumn("Kode Barang");
    model.addColumn("Stok Sistem");
    model.addColumn("Stok Fisik");
    model.addColumn("Selisih");

    try{

        Connection conn =
        koneksi.getConnection();

        String sql =
        "SELECT so.id_opname, " +
        "b.kode_barang, " +
        "so.stok_sistem, " +
        "so.stok_fisik, " +
        "so.selisih " +
        "FROM stock_opname so " +
        "JOIN barang b " +
        "ON so.id_barang=b.id_barang";

        PreparedStatement pst =
        conn.prepareStatement(sql);

        ResultSet rs =
        pst.executeQuery();

        while(rs.next()){

            model.addRow(new Object[]{

                rs.getString("id_opname"),
                rs.getString("kode_barang"),
                rs.getString("stok_sistem"),
                rs.getString("stok_fisik"),
                rs.getString("selisih")

            });

        }

        tblOpname.setModel(model);

    }catch(Exception e){

        System.out.println(e);

    }

}
