    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        try{

        Connection conn =
        koneksi.getConnection();

        int stokSistem =
        Integer.parseInt(
        txtStokSistem.getText());

        int stokFisik =
        Integer.parseInt(
        txtStokFisik.getText());

        int selisih =
        stokFisik - stokSistem;

        txtSelisih.setText(
        String.valueOf(selisih));

        String sql =
        "INSERT INTO stock_opname" +
        "(id_barang,tanggal,stok_sistem,stok_fisik,selisih) " +
        "SELECT id_barang,CURDATE(),?,?,? " +
        "FROM barang WHERE kode_barang=?";

        PreparedStatement pst =
        conn.prepareStatement(sql);

        pst.setInt(1,stokSistem);
        pst.setInt(2,stokFisik);
        pst.setInt(3,selisih);

        pst.setString(
        4,
        cmbBarang.getSelectedItem().toString());

        pst.executeUpdate();

        // update stok sesuai fisik

        String sqlUpdate =
        "UPDATE barang " +
        "SET stok=? " +
        "WHERE kode_barang=?";

        PreparedStatement pstUpdate =
        conn.prepareStatement(sqlUpdate);

        pstUpdate.setInt(1,stokFisik);

        pstUpdate.setString(
        2,
        cmbBarang.getSelectedItem().toString());

        pstUpdate.executeUpdate();

        JOptionPane.showMessageDialog(
        this,
        "Stock Opname berhasil");

        tampilOpname();

    }catch(Exception e){

        JOptionPane.showMessageDialog(
        this,
        e.getMessage());

    }
    }//GEN-LAST:event_btnSimpanActionPerformed

