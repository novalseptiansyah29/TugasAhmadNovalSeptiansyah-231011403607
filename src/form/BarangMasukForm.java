    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        try{

        Connection conn =
        koneksi.getConnection();

        String tanggal =
        new SimpleDateFormat("yyyy-MM-dd")
        .format(jDateChooser1.getDate());

        String kodeBarang =
        cmbBarang.getSelectedItem().toString();

        int jumlah =
        Integer.parseInt(
        txtJumlah.getText());

        // Simpan ke barang_masuk

        String sqlMasuk =
        "INSERT INTO barang_masuk(id_barang,tanggal,jumlah) " +
        "SELECT id_barang, ?, ? " +
        "FROM barang WHERE kode_barang=?";

        PreparedStatement pstMasuk =
        conn.prepareStatement(sqlMasuk);

        pstMasuk.setString(1,tanggal);
        pstMasuk.setInt(2,jumlah);
        pstMasuk.setString(3,kodeBarang);

        pstMasuk.executeUpdate();
        tampilMasuk();

        // Update stok

        String sqlUpdate =
        "UPDATE barang " +
        "SET stok = stok + ? " +
        "WHERE kode_barang=?";

        PreparedStatement pstUpdate =
        conn.prepareStatement(sqlUpdate);

        pstUpdate.setInt(1,jumlah);
        pstUpdate.setString(2,kodeBarang);

        pstUpdate.executeUpdate();

        javax.swing.JOptionPane
        .showMessageDialog(
        this,
        "Barang masuk berhasil disimpan");

        txtJumlah.setText("");

    }catch(Exception e){

        javax.swing.JOptionPane
        .showMessageDialog(
        this,
        e.getMessage());

    }
    }//GEN-LAST:event_btnSimpanActionPerformed

