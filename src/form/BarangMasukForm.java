    private void loadBarang(){

    try{

        Connection conn =
        koneksi.getConnection();

        String sql =
        "SELECT kode_barang FROM barang";

        PreparedStatement pst =
        conn.prepareStatement(sql);

        ResultSet rs =
        pst.executeQuery();

        cmbBarang.removeAllItems();

        while(rs.next()){

            cmbBarang.addItem(
            rs.getString("kode_barang"));

        }

    }catch(Exception e){

        System.out.println(e);

    }

}
    private void tampilMasuk(){

    DefaultTableModel model =
    new DefaultTableModel();

    model.addColumn("ID");
    model.addColumn("Kode Barang");
    model.addColumn("Tanggal");
    model.addColumn("Jumlah");

    try{

        Connection conn =
        koneksi.getConnection();

        String sql =
        "SELECT bm.id_masuk, " +
        "b.kode_barang, " +
        "bm.tanggal, " +
        "bm.jumlah " +
        "FROM barang_masuk bm " +
        "JOIN barang b " +
        "ON bm.id_barang=b.id_barang";

        PreparedStatement pst =
        conn.prepareStatement(sql);

        ResultSet rs =
        pst.executeQuery();

        while(rs.next()){

            model.addRow(new Object[]{

                rs.getString("id_masuk"),
                rs.getString("kode_barang"),
                rs.getString("tanggal"),
                rs.getString("jumlah")

            });

        }

        tblMasuk.setModel(model);

    }catch(Exception e){

        System.out.println(e);

    }

}

